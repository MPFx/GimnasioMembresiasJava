package enums;

/**
 * Enumeración que representa el estado de pago de una membresía.
 * 
 * @author ISC Israel de Jesús Mar Parada (MPFx++)
 * @version 1.0.0
 */
public enum EstadoPago {
    /** Pago al día */
    AL_DIA,
    /** Pago atrasado */
    ATRASADO,
    /** Membresía suspendida por falta de pago */
    SUSPENDIDO
}
