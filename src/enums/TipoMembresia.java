package enums;

/**
 * Enumeración que define los tipos de membresía disponibles en el gimnasio.
 * 
 * @author ISC Israel de Jesús Mar Parada (MPFx++)
 * @version 1.0.0
 */
public enum TipoMembresia {
    /** Membresía básica - acceso solo a sala de pesas */
    BASICA,
    /** Membresía premium - acceso a todas las áreas */
    PREMIUM,
    /** Membresía VIP - beneficios exclusivos */
    VIP,
    /** Membresía para estudiantes - precio especial */
    ESTUDIANTE,
    /** Membresía corporativa - para empresas */
    CORPORATIVA
}
